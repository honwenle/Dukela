<template>
  <div>
    <d-header>对公账户</d-header>
    <div class="cell">都克拉账户</div>
    <div class="group">
      <div class="cell">
        <div>用户名：浙江都克拉网络科技有限公司</div>
        <div>银行：中国农业银行</div>
        <div>开户行：*****</div>
        <div>账号：123456</div>
      </div>
    </div>
    <div class="cell">打款账户</div>
    <div class="group buy-group">
      <div class="cell">请输入打款账号</div>
      <div class="cell cell-input">
        <input type="text" v-model="bankno" placeholder="请输入账号">
      </div>
    </div>
    <div class="btn-full" @click="ok">确定</div>
  </div>
</template>
<script>
export default {
  data() {
    return {
      bankno: ''
    }
  },
  computed: {
    id() {
      return this.$store.state.OrderDetail.ID
    }
  },
  methods: {
    async ok() {
      if (this.bankno == '') {
        this.$vux.toast.text('请输入打款账户')
      }
      let data = await this.$store.dispatch('dklPay', {
        OrderID: this.id,
        BlankNo: this.bankno
      })
      if (data.Code == 1) {
        this.$vux.alert.show({
          content: '请务必24小时之内打款！',
          onHide () {
            console.log('我知道了')
          }
        })
      } else {
        this.$vux.toast.text(data.Message)
      }
    }
  }
}
</script>

